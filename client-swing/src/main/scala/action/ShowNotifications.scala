package ckit
package client
package swing
package action

import javax.swing._
import scala.swing._

/** Shows a dialog with all previous notification messages. */
object ShowNotifications extends scala.swing.Action("show") {
  override def apply {
    // preparing the dialog
    val dialog = new JDialog(SwingClient.top.peer, "Notifications", false)
    dialog.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE)
    dialog.setPreferredSize(new Dimension(640, 480))

    // preparing the display of the notifications
    val notifications = new JTextArea(StatusBar.notifications)
    notifications.setEditable(false)
    dialog.add(new JScrollPane(notifications))

    // finishing and showing the dialog
    dialog.pack()
    dialog.setLocationRelativeTo(SwingClient.top.peer)
    dialog.setVisible(true)
  }
}
