package ckit
package client
package swing

import scala.swing._

import akka.actor.ActorSystem

object SwingClient extends SwingApplication {
  val system = ActorSystem("ckit")

  def startup(args: Array[String]) {
    val top = new MainFrame(null) {
      override def closeOperation() {
        SwingClient.quit()
      }
    }

    top.title = "ClusterKit"

    val tabbed = new TabbedPane
    top.contents = tabbed

    tabbed.pages += new TabbedPane.Page("Welcome", new Label("... this is ClusterKit"))

    top.pack()
    top.visible = true
  }

  override def quit() {
    system.shutdown()
    sys.exit(0)
  }
}
